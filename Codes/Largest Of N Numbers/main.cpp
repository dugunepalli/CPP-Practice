//Largest in a N numbers

#include<bits/stdc++.h>
using namespace std;

int main()
{
    //Temporary Integer To Start Comaoring n numbers
    int Number;
    cout<<"Enter a Number: ";
    cin>>Number;
    int maximum=Number;
    while(Number!=-1)
    {
        if(Number>maximum)
        {
            maximum=Number;
        }
        cout<<"enter A Number in the Loop to Compare: ";
        cin>>Number;
    }
    cout<<"The Maximum of the Given  numbers is: "<<maximum;
}
